    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        // TODO add your handling code here:
          int selectedRow = datatable.getSelectedRow();
    if (selectedRow != -1) {
        TotalService vehicle = vehicleList.get(selectedRow);
        vehicle.setVehicleType(vehiclenamefield.getText());
        vehicle.setVehicleCompany(vehiclecompanyfield.getText());
        vehicle.setCustomerName(Cusnamefield.getText());
        vehicle.setCustomerNumber(Integer.parseInt(phonenumberfield.getText()));
        vehicle.setServiceStatus(jTextField5.getText());
        loadVehicleListToTable();
        
    } else {
        // Show a message if no row is selected
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a vehicle to update.");
    }
    }//GEN-LAST:event_updatebuttonActionPerformed

    private void DeletebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletebuttonActionPerformed
        // TODO add your handling code here:
         int selectedRow = datatable.getSelectedRow();
    if (selectedRow != -1) {
        vehicleList.remove(selectedRow);
        loadVehicleListToTable();
    } else {
        // Show a message if no row is selected
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a vehicle to delete.");
    }
    
   
    }//GEN-LAST:event_DeletebuttonActionPerformed

