    private void loadVehicleListToTable(){
        DefaultTableModel model = (DefaultTableModel) datatable.getModel();
        
        model.setRowCount(0);
        
        for(TotalService vehicle : vehicleList){
            model.addRow(new Object[]{
                vehicle.getVehicleNumber(),
                vehicle.getVehicleType(),
                vehicle.getVehicleCompany(),
                vehicle.getCustomerName(),
                vehicle.getCustomerNumber(),
                vehicle.getServiceStatus()
        });
        }
    }
    
